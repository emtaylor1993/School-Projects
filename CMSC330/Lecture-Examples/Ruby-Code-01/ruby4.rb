# Function example

def trueOrFalse() 
    puts "True of False"
    if nil then
       puts "nil detected"
    end
 
    if false then
       puts "false detected"
    end
 
    if 0 then
       puts "0 triggered (No!!!!)" 
    end
 
    if 1.1 then
       puts "1.1. triggered"
    end
 
    if -1.1 then
       puts "-1.1. triggered"
    end
 
    if "Maria" then
       puts "Maria triggered"
    end
end
 
trueOrFalse()
 